package common.enums;

/**
 * An enumeration representing the four cardinal directions.
 * This enum is used to indicate the position relative to a reference point, such as "ABOVE", "BELOW", "LEFT", and "RIGHT".
 */
public enum Direction {
    ABOVE,  // Indicates the position above the reference point
    BELOW,  // Indicates the position below the reference point
    LEFT,   // Indicates the position to the left of the reference point
    RIGHT   // Indicates the position to the right of the reference point
}